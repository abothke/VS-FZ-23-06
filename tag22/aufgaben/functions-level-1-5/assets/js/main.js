function math(valOne, valTwo){
    let multiplication = valOne * valTwo;
    let division = valOne / valTwo;
    console.log(`Multiplikation von ${valOne} und ${valTwo}: ${multiplication}`);
    console.log(`Division von ${valOne} und ${valTwo}: ${division}`);
}

math(10 ,2);
math(30, 20);
math(100, 100);
math(5, 0);
console.log("Wer teilt bitte durch 0?!");
math(45, 173);
math(1, 1000);
